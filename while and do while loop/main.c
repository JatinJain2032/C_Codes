#include <stdio.h>

int main()
{
    int i=1; // Initializtion

    while(i<=5) // condition
    {
        printf("I am amazing \n");
        i++; // increment and decrement
    }

    // do while

    int j=1; // Initializtion

    do
    {
        printf("I am Different \n");
        j++; // increment and decrement
    }
    while(j<=5); // condition
    return 0;
}
